let add = function (a, b) {
  if (Number.isInteger(a) && Number.isInteger(b)) {
    return a + b;
  } else {
    return "please put number type argument";
  }
};

let subtract = function (a, b) {
  if (Number.isInteger(a) && Number.isInteger(b)) {
    return a - b;
  } else {
    return "please put number type argument";
  }
};

let multiply = function (a, b) {
  if (Number.isInteger(a) && Number.isInteger(b)) {
    return a * b;
  } else {
    return "please put number type argument";
  }
};

let divide = function (a, b) {
  if (Number.isInteger(a) && Number.isInteger(b)) {
    return a / b;
  } else {
    return "please put number type argument";
  }
};
